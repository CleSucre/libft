#include "../includes/libft.h"

size_t	ft_nbrlen(long n)
{
	size_t	i;

	i = 1;
	if (0 > n)
	{
		n *= -1;
		i++;
	}
	while (9 < n)
	{
		n /= 10;
		i++;
	}
	return (i);
}
